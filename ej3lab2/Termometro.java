public class Termometro {
    private int temp1;
    private int temp2;

    public Termometro(int temp1, int temp2) {
        this.temp1 = temp1;
        this.temp2 = temp2;
    }

    //getter

    public int getTemp1() {
        return temp1;
    }

    public int getTemp2() {
        return temp2;
    }

    //Setter


    public void setTemp1(int temp1) {
        this.temp1 = temp1;
    }

    public void setTemp2(int temp2) {
        this.temp2 = temp2;
    }
}
